      <h2>About Us</h2>
	  </div>
	<div class="col-md-12 col-md-offset-0" style="text-align: justify;">
      <p>Moving legacy software systems to cloud platforms is an ever popular option. But, such an endeavour may not be hazard-free and demands a proper understanding of requirements and risks involved prior to taking any actions. The time is indeed ripe to undertake a realistic view of what migrating systems to the cloud may offer, an understanding of exceptional situations causing system quality goal failure, and insights on countermeasures. The cloud migration body of knowledge, although is useful, is dispersed over the current literature. It is hard for busy practitioners to digest, synthesize, and harness this body of knowledge into practice in a scenario of integrating legacy systems with cloud services. We address this issue by creating an innovative synergy between the approaches evidence-based software engineering and goal-oriented modelling. We develop an evidential repository of commonly occurred obstacles and platform agnostic resolution tactics related to making systems cloud-enabled. The repository can be further utilized during the systematic goal-obstacle elaboration of any cloud migration scenarios.  
		<br/><br/>This website is based on this premise that endeavours towards cloud migration are sometimes rewarding or challenging along with many lessons learned along the way. Reusing these lessons in different scenarios is a promising approach in a better exploration of uncertain risks against cloud adoption goals and reliability of decision outcomes. In this regard, our proposed framework harnesses a synergy between evidence-based software engineering and goal-oriented modelling approaches. The proposed framework comprises an itemized evidence-based repository and a cloud adoption goal-obstacle procedure utilizing the repository information. This is the first attempt turning the existing body of knowledge of cloud enablement into a concise, accessible, and a reusable source. This has not been a feature of the past research.
		<br/><br/>The website has several tables which includes cloud migration type, studies used for developing the knowledge repository of the framework, platform agnostic goals for cloud adoption, risks/obstacles associated with every goal, and obstacle resolution tactics for resolving each identified obstacle.
		<br/><br/>Users can get recommendations on resolving the obstacles by first identifying the goal that they would like their legacy system to achieve while adopting cloud. For each goal selected, the website will show the associated risks. After the risks associated with the goals have been displayed to the user, he can select the risks he would like to resolve. Finally the resolution tactic for every obstacle will be displayed to the user and he can make final selection about the obstacle resolution tactics.
	  </p>
